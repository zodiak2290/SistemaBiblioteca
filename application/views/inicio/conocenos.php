		         <p><strong>BIBLIOTECA  </strong></p>
		         <p align="justify"> La Biblioteca Pública Central de Oaxaca Margarita Maza de Juárez se encuentra a dos cuadras del zócalo de la ciudad
					sobre la calle Macedonio Alcalá, en esquina con calle Morelos. Sus instalaciones ocupan una casa de principios del XVIII,
					representativa de la arquitectura civil de la ciudad. Fue remodelada en el periodo contemporáneo por los arquitectos 
					Enrique de Esesarte y Jorge Torres. Inaugurada formalmente el 29 de octubre de 1985, como un organismo dependiente del sector público, 
					actualmente forma parte de la Dirección General de Bibliotecas y está adscrita a la Secretaría de las Culturas y Artes de Oaxaca.</p>
		        <p align="justify"> Encabeza como biblioteca central, a una red estatal de bibliotecas a cargo de la Coordinación Estatal de Bibliotecas.
				Existe una trama de bibliotecas de la ciudad, del sector público y de la sociedad civil, denominada Círculo de Bibliotecas de Oaxaca, de la que también forma parte.
				La Biblioteca Pública tiene, además de su acervo general, un acervo valioso de libros históricos sobre temas y autores oaxaqueños, conformada por varias colecciones de diverso origen.
				Entre estos materiales resguarda memorias manuscritas del General Ignacio Mejía, periódicos, folletos, boletines y revistas oaxaqueñas de época como La Crónica (1907), Oaxaca Moderno (1923) y el Boletín de la Biblioteca Pública del Estado (1920).
				En sus instalaciones se llevan a cabo presentaciones de libros, lectura de obra poética y literaria, exposiciones, encuentros, foros, charlas, conferencias y diversas actividades culturales.
				Sus instalaciones son visitadas por estudiantes, investigadores y público en general, pues es de acceso gratuito.</p>
				<p><strong> NUESTRA MISIÓN </strong></p> 
				<p align="justify">Proporcionar a la comunidad en general recursos documentales y servicios de información de manera eficiente, libre, gratuita y oportuna, que permitan apoyar las tareas sustantivas de docencia, investigación, difusión, recreación y extensión del
				conocimiento y la cultura, así como hacer extensivos estos beneficios a otras instituciones y bibliotecas de la red estatal.</p>
		        
